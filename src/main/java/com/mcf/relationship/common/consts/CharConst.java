package com.mcf.relationship.common.consts;

/**
 * 符号常量
 *
 * @author lizhenjie
 * @date 7/10/23 5:11 下午
 */
public interface CharConst {
    /**
     * 空字符串
     */
    String EMPTY = "";

    /**
     * 空格
     */
    String SPACE = " ";

    /**
     * 点
     */
    String DOT = ".";

    /**
     * 斜杠
     */
    String SLASH = "/";

    /**
     * 反斜杠
     */
    String BACKSLASH = "\\";

    /**
     * 回车符
     */
    String CR = "\r";

    /**
     * 换行符
     */
    String LF = "\n";

    /**
     * 连接符
     */
    String DASHED = "-";

    /**
     * 下划线
     */
    String UNDERLINE = "_";

    /**
     * 逗号
     */
    String COMMA = ",";

    /**
     * 花括号(左)
     */
    String DELIMITER_START = "{";

    /**
     * 花括号(右)
     */
    String DELIMITER_END = "}";

    /**
     * 中括号(左)
     */
    String BRACKET_START = "[";

    /**
     * 中括号(右)
     */
    String BRACKET_END = "]";

    /**
     * 双引号
     */
    String DOUBLE_QUOTES = "\"";

    /**
     * 单引号
     */
    String SINGLE_QUOTE = "'";

    /**
     * 与
     */
    String AMP = "&";

    /**
     * 冒号
     */
    String COLON = ":";

    /**
     * 艾特
     */
    String AT = "@";

    /**
     * 井号
     */
    String WELL = "#";

    /**
     * 等号
     */
    String EQUAL = "=";

    /**
     * 分号
     */
    String SEMICOLON = ";";

    /**
     * 问号
     */
    String QUESTION = "?";

}
